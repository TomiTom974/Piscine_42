#include "unistd.h"

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int ac, char **av)
{
	int arg;

	arg = 1;
	while (arg < ac)
	{
		ft_putstr(av[arg]);
		ft_putchar('\n');
		arg++;
	}
	return (0);
}
